namespace VectorTiles
{
    public enum StyleType
    {
        /// <summary>
        /// Style is for a raster source
        /// </summary>
        Raster,
        /// <summary>
        /// Style is for filling an area
        /// </summary>
        Fill,
        /// <summary>
        /// Style is for drawing a line
        /// </summary>
        Line,
        /// <summary>
        /// Style is for drawing a symbol
        /// </summary>
        Symbol,
        /// <summary>
        /// Style is for drawing a caption
        /// </summary>
        Caption,
        /// <summary>
        /// Style is for drawing a symbol with caption
        /// </summary>
        SymbolWithCaption
    }
}
